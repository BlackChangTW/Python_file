#資料 程式的基本單位
#數字
1234
2.5

#字串 任意的文字
"測試中文"
"Hello World"

#布林值 正確(True)和錯誤(False)
True 
False
 
#有順序 可動的列表 List
[1,2,3]
["Hello","World"]

#有順序 不可動的列表 Tuple
(1,2,3)
("Hello","World")

#集合 無順序的資料集合
{1,2,3}
{"Hello","World"}

#字典Dictionary 鑑值對(Key-Value Pair)的集合
{"apple":"蘋果","data":"資料"}

#變數 用來儲存資料的自訂名稱
#變數名稱 = 資料
data  = 3

#print(資料)
print(3)
print("Hello World")
print(True)
print([1,2,3])
print((1,2,3))
print({1,2,3})
print({"apple":"蘋果","data":"資料"})
print(data)

#取代舊的資料
y = "y第一個內容"
print(y)
y = "y二次宣告後的內容"
print(y)